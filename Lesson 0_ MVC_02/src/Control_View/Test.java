    public void hienThiBang() {
        dtm = new DefaultTableModel();
        dtm.addColumn("Ma sinh vien");
        dtm.addColumn("Ten sinh vien");
        dtm.addColumn("Lop sinh vien");
        dsSinhVien = sinhVienDAO.getAll();
        dtm.setRowCount(0);
        dsSinhVien.forEach(t -> {
            String rowData[] = {t.getMaSV() + "", t.getTenSV(), t.getLopSV()};
            dtm.addRow(rowData);
        });
        jtDSSinhVien.setModel(dtm);
    }

