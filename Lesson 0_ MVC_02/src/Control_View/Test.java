    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String maString = jtfMa.getText();
        String ten = jtfTen.getText();
        String lop = jtfLop.getText();
        int ma = 0;
        try {
            ma = Integer.parseInt(maString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Ma la 1 so nguyen duong");
            return;
        }
        if (ma <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Ma la 1 so nguyen duong");
            return;
        }
        SinhVien sinhVien = new SinhVien(ma, ten, lop);
        if (sinhVienDAO.themMoi(sinhVien)) {
            JOptionPane.showMessageDialog(rootPane, "Them moi thanh cong");
            hienThiBang();
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "Them moi that bai");
    }//GEN-LAST:event_jButton1ActionPerformed

